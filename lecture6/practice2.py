def array_compact(n):
    print("*" * n)
    

array_compact(10) # **********
array_compact(3)  # ***
array_compact(5)  # *****

def degree(n, dgr):
    print(n**dgr)

degree(2, 2)  # 4
degree(2, 10) # 1024
degree(10, 0) # 1

